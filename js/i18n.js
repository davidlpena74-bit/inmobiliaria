let currentLang = localStorage.getItem("weperty_lang") || "es";

function updateContent() {
    // 1. Textos estáticos marcados con data-i18n
    document.querySelectorAll("[data-i18n]").forEach(el => {
        const key = el.getAttribute("data-i18n");
        let value = translations[currentLang][key];
        if (value) {
            if (Array.isArray(value)) {
                value = value[Math.floor(Math.random() * value.length)];
            }
            if (el.tagName === "INPUT") {
                el.placeholder = value;
            } else if (el.hasAttribute('data-i18n-html')) {
                el.innerHTML = value;
            } else {
                el.textContent = value;
            }
        }
    });

    // 2. Actualizar bandera e indicador de idioma en el nav
    const langFlags = {
        es: "https://flagcdn.com/w40/es.png",
        en: "https://flagcdn.com/w40/gb.png",
        de: "https://flagcdn.com/w40/de.png",
        nl: "https://flagcdn.com/w40/nl.png"
    };

    document.querySelectorAll(".lang-menu").forEach(menu => {
        const dropdown = menu.querySelector(".lang-dropdown");
        const activeWrapper = menu.querySelector(".lang-active");
        
        const indicatorHtml = `<img src="${langFlags[currentLang]}" width="18" style="vertical-align: middle; margin-right: 5px;"> <i class="fa-solid fa-chevron-down" style="font-size: 10px; margin-left: 2px;"></i>`;
        
        if (activeWrapper) {
            activeWrapper.innerHTML = indicatorHtml;
        } else {
            // Fallback for old structure: only replace what's before the dropdown
            const dropdownBackup = dropdown ? dropdown.cloneNode(true) : null;
            menu.innerHTML = `<span class="lang-active">${indicatorHtml}</span>`;
            if (dropdownBackup) menu.appendChild(dropdownBackup);
        }
    });
}

function setLanguage(lang) {
    // Close all open dropdowns regardless of whether the language changed
    document.querySelectorAll('.lang-menu').forEach(menu => {
        menu.classList.add('lang-selecting');
        const onMouseLeave = () => {
            menu.classList.remove('lang-selecting');
            menu.removeEventListener('mouseleave', onMouseLeave);
        };
        menu.addEventListener('mouseleave', onMouseLeave);
    });

    if (lang === currentLang) return;
    
    currentLang = lang;
    localStorage.setItem("weperty_lang", lang);
    updateContent();

    if (typeof loadWebProperties === "function") {
        loadWebProperties();
    }

    // 🚩 Inmueble.js sync: Si estamos en modo detalle y hay datos cargados, re-renderizar
    if (typeof renderProperty === "function" && window.currentProperty) {
        renderProperty(window.currentProperty);
    }
}

/**
 * t_prop(prop, field)
 * Función de conveniencia para obtener traducciones de propiedades de Supabase
 */
function t_prop(prop, field) {
    if (currentLang === "es") return prop[field]; // Default es español
    
    // Intentar buscar en la columna 'traducciones' (JSONB)
    if (prop.traducciones && prop.traducciones[currentLang] && prop.traducciones[currentLang][field]) {
        return prop.traducciones[currentLang][field];
    }
    
    // Fallback: Si no hay traducción, devolver el original
    return prop[field];
}

